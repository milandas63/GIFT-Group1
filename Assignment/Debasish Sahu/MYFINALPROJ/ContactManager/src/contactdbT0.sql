DROP DATABASE IF EXISTS contactdbT0;
CREATE DATABASE IF NOT EXISTS contactdbT0;

USE contactdbT0;
DROP TABLE IF EXISTS category;
CREATE TABLE IF NOT EXISTS category (
    Cid INT(6) NOT NULL AUTO_INCREMENT,
    Cname VARCHAR(60) NOT NULL UNIQUE,
    PRIMARY KEY(Cid)
);



CREATE TABLE location(
    Lid INT(6) NOT NULL AUTO_INCREMENT,
    LNAME VARCHAR(60) NOT NULL UNIQUE,
    PRIMARY KEY(Lid)
);

CREATE TABLE contact_d(
    cnID INT(6) NOT NULL AUTO_INCREMENT,
    cnNAME VARCHAR(60) NOT NULL UNIQUE,
    GENDER ENUM("M","F") NOT NULL,
    catID INT(6) NOT NULL REFERENCES category(catID),
    locID INT(6) NOT NULL REFERENCES location(locID),
    addr VARCHAR(150) NOT NULL,
    pinCD VARCHAR(50) NOT NULL,
    email1 VARCHAR(320) DEFAULT NULL UNIQUE,
    email2 VARCHAR(320) DEFAULT NULL UNIQUE,
    mobNUM1 VARCHAR(15) DEFAULT NULL UNIQUE,
    mobNUM2 VARCHAR(15) DEFAULT NULL UNIQUE,
    PRIMARY KEY(cnID)
);

